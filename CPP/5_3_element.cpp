#include<bits/stdc++.h>
using namespace std;

int main(){
    
    string s = "Hello";

    cout << "s -> " << s << endl;
    cout << "s[0] -> " << s[0] << endl;
    cout << "s.at(1) -> " << s.at(1) << endl;

    cout << "s.front() -> " << s.front() << endl;
    cout << "s.back() -> " << s.back() << endl;
    cout << "s[s.size()-1] -> " << s[s.size()-1] << endl;
    cout << "s.at(s.size()-1) -> " << s.at(s.size()-1) << endl;
    
    return 0;
}

